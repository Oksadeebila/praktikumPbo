package Praktikum3;

public class PerulanganWhile {

	public static void main(String[] args) {
		
		//inisialisasi angak = 9, ketka angkanya < 10, maka perulangan stop
		int angka = 9;
		//while(kondisi){perintah; increment}
		while(angka<=10) {
			System.out.println("Angka ke-"+angka);
			angka++;//increment 
		}
	}

}
